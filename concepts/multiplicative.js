/*
multiplicative operators
The operators * , / , and % are called the multiplicative operators. 
They have the same precedence and are syntactically left-associative (they group left-to-right). */

console.log(3 * 4);
// expected output: 12

console.log(-3 * 4);
// expected output: -12

console.log('3' * 2);
// expected output: 6

console.log('foo' * 2);
// expected output: NaN