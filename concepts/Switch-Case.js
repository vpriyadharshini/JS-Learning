//Switch Statement
var price_=1000;
switch (price_) {
  case 1000:
     console.log("You purchased items for Rs.1000");
    break;
  case 2000:
    console.log("You purchased items for Rs.2000");
    break;
  case 3000:
      console.log("You purchased items for Rs.3000");
      break;
  default:
    console.log("Invalid Price Amount");
}